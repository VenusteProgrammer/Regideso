        <h1> A Propos de nous </h1>


        <p style="text-align: justify;"><span style="color: #000000;">La Régie de Production et de Distribution d’eau et
                d’électricité du Burundi, REGIDESO, en sigle, a été créée le 22 juin 1962 par l’ordonnance législative
                053/113.&nbsp;C’est une entreprise à caractère socio-commerciale.</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">Sa forme juridique et son cadre institutionnel ont
                évolué avec le temps, passant d’une Administration personnalisée, puis d’un Etablissement Public à caractère
                commercial et industriel jouissant d’une personnalité juridique et d’une autonomie financière, à la forme
                d’une société à participation publique depuis le 5 septembre 1997. Elle est sous la tutelle du Ministère de
                l’Energie et des Mines. Son capital social est fixé à 55.450.361.914 de Fbu et son personnel s’élève à 1272
                agents.<sup>2</sup></span></p>
        <p style="text-align: justify;"><span style="color: #000000;">Au moment de sa création, il n’y avait aucune Centrale
                Hydroélectrique. Elle fournissait de l’énergie électrique grâce à quelques groupes électrogènes pour les
                centres de l’intérieur, et deux Centrales Thermiques respectivement pour Bujumbura et Gitega et à l’énergie
                électrique fournie par la CHE RUZIZI 1 via le Poste SNEL. Actuellement la REGIDESO fournit de l’énergie
                électrique grâce aux centrales hydro-électriques et thermiques. En ce qui concerne l’eau, seules les sources
                de NTAHANGWA, GATUNGURU et KINUKE alimentaient toute la ville de Bujumbura tandis que les centres de
                l’intérieur étaient alimentés en eau par l’Agence Internationale de Développement Rural (AIDR).</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">Avant l’indépendance, les attributions du service de
                Hydraulique étaient confiées à l’Agence Internationale de Développement Rural (AIDR) avec une mission
                d’approvisionnement en eau potable à l’intérieur du pays, alors qu’en milieu urbain, le service de
                production et distribution d’eau et électricité était assurée par la «&nbsp;Régie de distribution d’eau et
                d’électricité du Congo Belge du Rwanda-Urundi.»<sup>3</sup></span></p>
        <p style="text-align: justify;"><span style="color: #000000;">De 1962 à 1975 la REGIDESO avait procédait à la mise
                en place des assises de la société la recherche de partenaires au développement et la réalisation des études
                d’identification des projets.</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">De 1975 à 1992 C’était une année de croissance et de
                grande réalisation d’infrastructures. Avec l’aide de l’Etat et des bailleurs de fonds externes dont
                l’Allemagne, France, Chine, Banque mondiale etc. La REGIDESO a réalisé un réseau d’infrastructures fiables
                durant cette période qui lui a permis d’alimenter en eau potable et en électricité tous les centres Urbains
                du pays.</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">Depuis lors, la REGIDESO a énormément investi et ses
                immobilisations ont connu une grande expansion sur tout sur le territoire du Burundi. Sa forme juridique et
                son cadre institutionnel ont évolué avec le temps, passant d’une administration personnalisée, d’un
                établissement public à caractère commercial et industriel jouissant d’une personnalité juridique et d’une
                autonomie financière à la forme d’une société à participation publique depuis le 05 septembre 1977.</span>
        </p>
        <p style="text-align: justify;"><span style="color: #000000;">Dans le secteur de l’électricité toutes les centrales
                hydroélectriques dont dispose le pays (GikongeKayenziBuhigaSanzu&nbsp;MarangaraRweguraNyemanga et la
                centrale hydroélectrique communautaire de Rusizi) d’une capacité installée d’environ 40&nbsp;MW ont été
                construites en cette période. La dernière CHE qui a été mise en œuvre est Nyemanga en 1988&nbsp;; cette
                période correspond également à la construction et la mise en service de grandes lignes de transport et de
                distribution d’électricité.</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">Dans le secteur de l’eau cette période a été consacrée
                à l’accroissement de la capacité de production d’eau et aux réseaux de sa distribution. Il est également
                indiqué que dès l’indépendance à nos jours&nbsp;; des progrès remarquables ont été réalisés.</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">De 1992 à 2004 Les services administratifs, comptables
                et commerciaux érigés en Direction administratives et celles modifiée en Direction Administrative et
                Financière en 1978 sont scindés en Direction d’électricité et de l’eau en 1995, Le service des Ressources
                humaines a été formé en Direction des Ressources humaines dans les années 1995.Signalons que dans cette
                période l’entreprise a subi la crise sociopolitique qui a causé la destruction d’une partie importante de
                ses installations.</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">L’entreprise a éprouvé d’énormes difficultés de
                reconstruire ses infrastructures et faire face aux travaux de maintenance et entretien parce que les
                financements avaient tari (épuisés) suite au retrait des bailleurs de fonds alors qu’elle ne dispose pas de
                capacité propre d’autofinancement. Cependant les ressources humaines de cette entreprise ont démontré leurs
                capacités dans le rétablissement du service d’eau et d’électricité en ces moments durs.</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">De 2005 à 2014 la REGIDESO avait procédait à la
                reconstruction et le développement des infrastructures. C’est pendant cette période que celle-ci a commencé
                à réhabiliter les infrastructures endommagés ou vétustes et étendre les réseaux d’alimentation en eau et en
                électricité dans les nouveaux quartiers tant dans la ville de Bujumbura que dans l’intérieur du pays. C’est
                dans la période de fin 2011 et en mars 2012 que la REGIDESO a augmenté les prix de vente de façon que les
                ménages des plus gros consommateurs aient vu leur facture d’énergie multipliée par 3 passant de 006US/KW à
                018US/KW. Ces augmentations sont un des premiers résultats des actions menées par le gouvernement avec le
                soutien de la Banque&nbsp;Mondiale pour redresser la situation économique de la REGIDESO et la crédibilité
                vis-à-vis des futures investisseurs devant signer des contrats avec elle.</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">La REGIDESO avait procédé également à la location d’un
                groupe Diesel de 10&nbsp;MW<sup>5</sup>. Le besoin d’augmentation de la capacité est donc considérable et
                urgent. Or la législation Burundaise a ouvert des opportunités aux investisseurs. Suite à la loi 1/014 du 20
                août 2000 portant libéralisation et règlementation du secteur de l’eau potable et de l’électricité la
                REGIDESO n’a plus le monopole de la production électrique sur le territoire national. A ces puissances
                s’ajoutent l’importation de 3MW à travers un contrat d’achat entre la REGIDESO et la Société Nationale
                d’Electricité de la République Démocratique du Congo(SNEL) à partir de la centrale hydroélectrique commune
                de RUZIZI I et de 13.3MW à partir la Société Internationale d’Electricité des pays des grands Lacs(SINELAC)
                .</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">Selon la même source le Burundi est intégré à
                l’EasternAfrican Power Pool(EAPP). Ce plan d’interconnexion vise à interconnecter entre eux les pays de
                l’est africain de la Tanzanie jusqu’à l’Egypte. Ainsi les liaisons entre les pays permettront l’import ou
                l’export plus aisé d’énergie électrique entre les pays de la zone. C’est un débouché ouvert à une éventuelle
                production d’énergie hydraulique à faible coût au Burundi en direction d’autres pays. L’idée de créer à
                moyen et long terme «un marché commun de l’électricité» comme cela se fait en Europe et ailleurs avec un
                système de tarification harmonisé. La plupart des pays de l’EAC étant déjà interconnectés il reste à
                construire la ligne Burundi-Rwanda (ligne HT 220KV Kigoma-Ngozi-Gitega sur financement de l’UE et
                Burundi-Tanzanie. L’interconnexion avec les grands projets hydroélectriques régionaux et l’implication
                progressive du secteur privé représente stratégiquement l’avenir énergétique pour la région.</span></p>
        <p style="text-align: justify;"><span style="color: #000000;">Enfin durant l’année 2015 la REGIDESO a subi également
                une crise politique grâce aux manifestations qui se faisaient dans des différents quartiers de la ville de
                Bujumbura et cela a fait que les agents releveurs de la REGIDESO ne se rendent pas sur terrain pour relever
                les index sur les compteurs d’eau des clients afin d’établir les factures l’entreprise a été beaucoup
                perturbé car elle n’a pas eu l’occasion pour satisfaire sa clientèle quant au fourniture des unités Kash
                Power réparation des pannes dans des différents coins de la ville encaissement raccordement des clients
                établissement des devis etc.</span></p>
